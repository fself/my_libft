int		ft_sqrt(int nbr)
{
	int sqrt;

	if (nbr < 0)
		return (0);
	sqrt = 1;
	while (sqrt * sqrt < nbr)
		++sqrt;
	if (sqrt * sqrt == nbr)
		return (sqrt);
	return (0);
}
